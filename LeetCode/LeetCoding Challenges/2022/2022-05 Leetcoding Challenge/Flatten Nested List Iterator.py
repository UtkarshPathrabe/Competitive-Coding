class NestedIterator:
    def __init__(self, nestedList: [NestedInteger]):
        self._data = nestedList[::-1]
    
    def next(self) -> int:
        return self._data.pop().getInteger()
    
    def hasNext(self) -> bool:
        while self._data and not self._data[-1].isInteger():
            self._data += self._data.pop().getList()[::-1]
        return len(self._data) > 0

